import os

def limpiar_pantalla():
    # Limpia la consola dependiendo del sistema operativo
    os.system('cls' if os.name == 'nt' else 'clear')

def ejecutar_sistema():
    # En Python, en lugar de una matriz fija de [100, 4], 
    # usamos una lista que puede crecer din√°micamente.
    inventario = []
    contador_equipos = 0

    while True:
        print("\n--- SISTEMA DE INVENTARIO DE EQUIPOS DE RED ---")
        print("1. Agregar un Nuevo Equipo")
        print("2. Mostrar Inventario Completo")
        print("3. Buscar el Equipo por Tipo/Marca/Ubicacion")
        print("4. Salir")
        
        opcion = input("Ingrese la opci√≥n: ")

        # --- 1. Agregar Nuevo Equipo ---
        if opcion == "1":
            if len(inventario) < 100:
                print("\n--- Agregar Nuevo Equipo ---")
                tipo = input("Ingrese el Tipo (Switch, Router, Firewall, AP): ")
                marca = input("Ingrese la Marca: ")
                ubicacion = input("Ingrese la Ubicaci√≥n (Edificio/Sala): ")
                
                contador_equipos += 1
                # Creamos un diccionario para representar el equipo
                nuevo_equipo = {
                    "id": str(contador_equipos),
                    "tipo": tipo,
                    "marca": marca,
                    "ubicacion": ubicacion
                }
                inventario.append(nuevo_equipo)
                print(f"‚úÖ Equipo agregado con ID: {nuevo_equipo['id']}")
            else:
                print("‚ùå El inventario est√° lleno (m√°ximo 100 equipos).")

        # --- 2. Mostrar Inventario Completo ---
        elif opcion == "2":
            print("\n--- INVENTARIO DE EQUIPOS DE RED ---")
            if inventario:
                for equipo in inventario:
                    print(f"ID: {equipo['id']} | Tipo: {equipo['tipo']} | Marca: {equipo['marca']} | Ubicaci√≥n: {equipo['ubicacion']}")
            else:
                print("‚ö†Ô∏è El inventario est√° vac√≠o.")

        # --- 3. Buscar Equipo ---
        elif opcion == "3":
            print("\n--- Buscar Equipo ---")
            termino = input("Ingrese t√©rmino de b√∫squeda (Tipo, Marca o Ubicaci√≥n): ").lower()
            encontrado = False
            
            print("--- Resultados de B√∫squeda ---")
            for equipo in inventario:
                # Comparamos convirtiendo a min√∫sculas para facilitar la b√∫squeda
                if (termino in equipo['tipo'].lower() or 
                    termino in equipo['marca'].lower() or 
                    termino in equipo['ubicacion'].lower()):
                    
                    print(f"ID: {equipo['id']} | Tipo: {equipo['tipo']} | Marca: {equipo['marca']} | Ubicaci√≥n: {equipo['ubicacion']}")
                    encontrado = True
            
            if not encontrado:
                print(f"No se encontraron equipos que coincidan con '{termino}'.")

        # --- 4. Salir ---
        elif opcion == "4":
            print("Saliendo del sistema. ¬°Hasta Luego! üëã")
            break

        # --- Opci√≥n Inv√°lida ---
        else:
            print("‚ùå Opci√≥n no v√°lida. Intente de nuevo.")

        input("\nPresione Enter para continuar...")
        limpiar_pantalla()

if __name__ == "__main__":
    ejecutar_sistema()